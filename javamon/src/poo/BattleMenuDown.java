    public void enableAll(boolean b){
        this.jBTrocarPokemon.setEnabled(b);
        if(parent.personagemAtual.isItem1() && b){        
        this.jBItem1.setEnabled(true);
        }else{
            this.jBItem1.setEnabled(false);
        }
        if(parent.personagemAtual.isItem2() && b){
        this.jBItem2.setEnabled(true);
        } else{
            this.jBItem2.setEnabled(false);
        }
        this.jBAfraco1.setEnabled(b);
        this.jBAfraco2.setEnabled(b);
        this.jBAFt1.setEnabled(b);
        this.jBAFt2.setEnabled(b);
        
    }

